#include <iostream>

//app includes
#include "Engineering.h"

using namespace std;

///////////////////////////////////////////////////////////////////////////////
Engineering::Engineering( int rate, bool adv )
            :Employee(rate)
{
    _Advanced = adv;
}

///////////////////////////////////////////////////////////////////////////////
Engineering::Engineering( const Engineering& emp )
            :Employee(emp)
{
    _Advanced = emp._Advanced;
}

///////////////////////////////////////////////////////////////////////////////
Engineering::~Engineering()
{
    //nothing to deallocate
}

///////////////////////////////////////////////////////////////////////////////
int Engineering::ComputePay()
{
    int salary;
    if( _Advanced )
        salary = _HourlyRate * 8 * 5 * 52;
    else 
        salary = _HourlyRate * 8 * 5 * 50;
    return salary;
}

///////////////////////////////////////////////////////////////////////////////
void Engineering::Print()
{
    cout << "Engineering: " << ComputePay() << "\n";
}


