#include "Accounting.h"

using namespace std;

///////////////////////////////////////////////////////////////////////////////
Accounting::Accounting( int rate, bool certified )
            :Employee( rate )
{
    _Certified = certified;
}

///////////////////////////////////////////////////////////////////////////////
Accounting::Accounting( const Accounting& emp )
            :Employee(emp)
{
    _Certified = emp._Certified;
}

///////////////////////////////////////////////////////////////////////////////
Accounting::~Accounting()
{
    //nothing to deallocate
}

///////////////////////////////////////////////////////////////////////////////
int Accounting::ComputePay()
{
    int salary;
    if( _Certified )
        salary = (_HourlyRate + 10) * 8 * 5 * 52;
    else 
        salary = Employee::ComputePay();
    return salary;
}


