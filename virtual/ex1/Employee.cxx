// system includes
#include <iostream>

//app includes
#include "Employee.h"

using namespace std;

///////////////////////////////////////////////////////////////////////////////
Employee::Employee( int rate )
{
    _HourlyRate = rate;
}

///////////////////////////////////////////////////////////////////////////////
Employee::Employee( const Employee& emp )
{
    _HourlyRate = emp._HourlyRate;
}

///////////////////////////////////////////////////////////////////////////////
Employee::~Employee()
{
    //nothing to deallocate
}

///////////////////////////////////////////////////////////////////////////////
int Employee::ComputePay()
{
    int yearlySalary = _HourlyRate * 8 * 5 * 52;
    return yearlySalary;
}

///////////////////////////////////////////////////////////////////////////////
void Employee::Print()
{
    int yearlySalary = _HourlyRate * 8 * 5 * 52;
    cout << yearlySalary << "\n";
}


